#include <stdio.h>

int main()
{
    int i, j, n = 5;

    for (j = 1; j <= 11; j++)
        printf("*");
    printf("\n");

    for (i = n - 1; i >= 1; i--)
    {
        for (j = 1; j <= i; j++)
            printf("*");

        for (j = 1; j <= (2 * (n - i) + 1); j++)
            printf(" ");

        for (j = 1; j <= i; j++)
            printf("*");

        printf("\n");
    }

    for (i = 1; i <= n - 1; i++)
    {
        for (j = 1; j <= i; j++)
            printf("*");

        for (j = 1; j <= (2 * (n - i) + 1); j++)
            printf(" ");

        for (j = 1; j <= i; j++)
            printf("*");

        printf("\n");
    }

    for (j = 1; j <= 11; j++)
        printf("*");

    return 0;
}